package com.gdn.venice.exportimport.inventory.dataexport;


/**
 *
 * @author Roland
 */
public class PutawayPrint {
	String putawayNo;
	String reffNo;
	String warehouseSkuId;
	String itemName;
	String storageCode;
	String qty;
	String warehouseCode;
	
	public String getPutawayNo() {
		return putawayNo;
	}
	public void setPutawayNo(String putawayNo) {
		this.putawayNo = putawayNo;
	}
	public String getReffNo() {
		return reffNo;
	}
	public void setReffNo(String reffNo) {
		this.reffNo = reffNo;
	}
	public String getWarehouseSkuId() {
		return warehouseSkuId;
	}
	public void setWarehouseSkuId(String warehouseSkuId) {
		this.warehouseSkuId = warehouseSkuId;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getStorageCode() {
		return storageCode;
	}
	public void setStorageCode(String storageCode) {
		this.storageCode = storageCode;
	}
	public String getQty() {
		return qty;
	}
	public void setQty(String qty) {
		this.qty = qty;
	}
	public String getWarehouseCode() {
		return warehouseCode;
	}
	public void setWarehouseCode(String warehouseCode) {
		this.warehouseCode = warehouseCode;
	}
	
}
